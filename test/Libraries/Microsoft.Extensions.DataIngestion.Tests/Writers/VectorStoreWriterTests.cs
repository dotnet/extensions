    [Fact]
    public async Task CanWriteChunksWithCustomDefinition()
    {
        string documentId = Guid.NewGuid().ToString();

        using TestEmbeddingGenerator<AIContent> testEmbeddingGenerator = new();
        using VectorStore vectorStore = CreateVectorStore(testEmbeddingGenerator);

        // User creates their own definition without using CreateDefaultCollectionDefinition,
        // using custom storage names to prove they can map to a pre-existing collection schema.
        VectorStoreCollectionDefinition definition = new()
        {
            Properties =
            {
                new VectorStoreKeyProperty(nameof(IngestionChunkVectorRecord.Key), typeof(Guid)) { StorageName = "custom_key", IsAutoGenerated = true },
                new VectorStoreVectorProperty<AIContent>(nameof(IngestionChunkVectorRecord.Embedding), TestEmbeddingGenerator<AIContent>.DimensionCount)
                {
                    StorageName = "custom_embedding",
                },
                new VectorStoreDataProperty(nameof(IngestionChunkVectorRecord.SerializedContent), typeof(string)) { StorageName = "custom_content" },
                new VectorStoreDataProperty(nameof(IngestionChunkVectorRecord.Context), typeof(string)) { StorageName = "custom_context" },
                new VectorStoreDataProperty(nameof(IngestionChunkVectorRecord.DocumentId), typeof(string))
                {
                    StorageName = "custom_documentid",
                    IsIndexed = true,
                },
            },
        };

        VectorStoreCollection<Guid, IngestionChunkVectorRecord> collection = vectorStore.GetCollection<Guid, IngestionChunkVectorRecord>("chunks-custom", definition);

        using VectorStoreWriter<IngestionChunkVectorRecord> writer = new(collection);

        IngestionDocument document = new(documentId);
        IngestionChunk chunk = TestChunkFactory.CreateChunk("custom schema content", document);

        List<IngestionChunk> chunks = [chunk];

        await writer.WriteAsync(chunks.ToAsyncEnumerable());

        IngestionChunkVectorRecord record = await writer.VectorStoreCollection
            .GetAsync(filter: record => record.DocumentId == documentId, top: 1)
            .SingleAsync();

        Assert.NotNull(record);
        Assert.NotEqual(Guid.Empty, record.Key);
        Assert.Equal(documentId, record.DocumentId);
        Assert.NotEmpty(record.SerializedContent);
    }

